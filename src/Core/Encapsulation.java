package Core;

public class Encapsulation {
	private String name;
	private int id;
	private float value;

	public void setname(String name) {
		this.name = name;

	}

	public void setname(int id) {
		this.id = id;
	}

	public void setfloat(float value) {
		this.value = value;
	}

	public String getname() {
		return name;

	}

	public int getid() {
		return id;
	}

	public float getvalue() {
		return value;
	}
}
